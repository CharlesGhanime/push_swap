#include "push_swap.h"

int		free(t_ps *ps)
{
	free(ps->a);
	free(ps->b);
	ps->len_a = 0;
	ps->len_b = 0;
	ps->args_nb = 0;
	ps->mv_nb = 0;
	ps->pivot = 0;
	free(ps);
	return (0);
}
